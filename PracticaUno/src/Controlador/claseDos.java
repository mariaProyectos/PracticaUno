package Controlador;

public class claseDos {
	String cadenaUno;
	int posicionCadena;
	int numeroCadena;

	public String getCadenaUno() {
		return cadenaUno;
	}

	public void setCadenaUno(String cadenaUno) {
		this.cadenaUno = cadenaUno;
	}

	public int getPosicionCadena() {
		return posicionCadena;
	}

	public void setPosicionCadena(int posicionCadena) {
		this.posicionCadena = posicionCadena;
	}

	public int getNumeroCadena() {
		return numeroCadena;
	}

	public void setNumeroCadena(int numeroCadena) {
		this.numeroCadena = numeroCadena;
	}

}
